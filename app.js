let myAge = 25; // my age
let earlyYears = 2; // early years
earlyYears = earlyYears * 10.5; // calculation for early years

let laterYears = myAge - 2; // my age - 2
laterYears = laterYears * 4; // calculation for later years

let myAgeInDogYears = earlyYears + laterYears; // sum of earlyYears and laterYears

let myName = 'Ali'; // name

let res = `My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`; // string interpolation

console.log(res);






